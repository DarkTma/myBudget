package com.example.mybudget1;

public class CategoryItem {
    private String name;
    private int price;
    private int id;

    public CategoryItem(int id, String name, int price) {
        this.id = id;
        this.name = name;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }

    public void setName(String name) {
        this.name = name;
    }
}
